 //sizeof operatörünü kullanarak her bir deðiþkenin boyutunu deðerlendirmeyi öðreneceksiniz.
 //You will learn to evaluate the size of each variable using the sizeof operator.
 
#include<stdio.h>
int main() {
    int intType;
    float floatType;
    double doubleType;
    char charType;
 
    // sizeof bir deðiþkenin boyutunu deðerlendirir sizeof evaluates the size of a variable
    printf("int Boyutu : %zu bytes\n", sizeof(intType));
    printf("float Boyutu : %zu bytes\n", sizeof(floatType));
    printf("double Boyutu : %zu bytes\n", sizeof(doubleType));
    printf("char Boyutu : %zu byte\n", sizeof(charType));
    
    return 0;
}
